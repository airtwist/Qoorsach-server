        <h2> Достоинства и недостатки</h2>
        <article>
            <br> Достоинства <br>
            <br>
           <ul><li> програма бесплатна;
               <li> программа не требует установки (для работы версии программы, написанной для ОС Windows, требуется наличие в системной папке /system32/drivers/ файла porttalk.sys);
               <li>за счёт прямого доступа к оборудованию скорость работы программы высока;
               <li> исполняемый файл программы имеет малый размер;
               <li>программа поддерживает множество различных тестов;
               <li> программа поддерживает интерфейс AHCI (в отличии, например, от программы MHDD; перед работой с программой MHDD требуется в меню BIOS переключаться на режим загрузки IDE).</ul>
